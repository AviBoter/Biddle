package com.example.biddle.Activites;

public class ExpireDate {
    private int year,month;


    public ExpireDate() {}

    public ExpireDate(int year, int month) {
        this.year = year;
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }
}
